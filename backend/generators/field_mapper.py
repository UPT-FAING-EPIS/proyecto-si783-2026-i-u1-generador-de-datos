def generate_by_field_name(field_name: str, table_name: str = None) -> any:
    key = field_name.lower().strip()
    table_key = (table_name or "").lower().strip()

    if table_key and table_key in TABLE_CONTEXT_MAP:
        table_overrides = TABLE_CONTEXT_MAP[table_key]
        if key in table_overrides:
            return table_overrides[key]()

    if key in FIELD_MAP:
        return FIELD_MAP[key]()

    return None
