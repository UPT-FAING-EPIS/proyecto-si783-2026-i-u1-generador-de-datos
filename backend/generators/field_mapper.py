def _unique_category(existing: set = None) -> str:
    available = [c for c in CATEGORY_NAMES if existing is None or c not in existing]
    if available:
        return random.choice(available)
    suffix = random.randint(100, 9999)
    base = random.choice(CATEGORY_NAMES)
    return f"{base} {suffix}"

